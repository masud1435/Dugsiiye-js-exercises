// Create a variable x with a value of 7 and a variable y with a value of 3. Perform all the arithmetic operations and log the results.

let x = 7;
let y = 3;
console.log(x+y); // output 10
console.log(x-y); // output 4
console.log(x*y); // output 21
console.log(x/y); // output 2.33333333
console.log(x%3); // output 1
console.log(x++); // output 7
console.log(x++); // output 8
console.log(y--); // output 3
console.log(y--); // output 2


